
public class unary_operator {

	public static void main(String[] args)
	
	{
		int i1 = -10;

	      System.out.println(i1);

	      

	      int i2 = 10;

	      System.out.println(i2++);



	      int i3 = 10;

	      System.out.println(++i3);



	      int i4 = 10;

	      System.out.println(i4--);



	      int i5 = 10;

	      System.out.println(--i5);

	  

	      int i6 = 10;

	      System.out.println(~i6);

	}

}
